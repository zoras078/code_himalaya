public class AddTwoNumbers {
    public static void main(String[] args) {
        // Two numbers to be added
        int num1 = 5;
        int num2 = 10;

        // Sum of num1 and num2
        int sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}

