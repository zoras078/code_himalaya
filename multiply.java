public class MultiplyTwoNumbers {
    public static void main(String[] args) {
        // Two numbers to be multiplied
        int num1 = 5;
        int num2 = 10;

        // Product of num1 and num2
        int product = num1 * num2;

        // Print the result
        System.out.println("The product of " + num1 + " and " + num2 + " is: " + product);
    }
}

